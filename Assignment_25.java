package done_assignments;

public class Assignment_25 {

	public static void main(String[] args) {
		//Write a program to print even & odd numbers from 0 to 100
		for(int i=0;i<=100;i++)
		{
			if (i%2==0)
			{
				System.out.println("This is Even number "+i);
			}
			else
			{
				System.out.println("This is Odd number "+i);
			}	
		}

	}

}
